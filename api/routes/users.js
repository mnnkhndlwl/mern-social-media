//follow a user

router.put("/:id/follow", async (req, res) => {
  if (req.body.userId !== req.params.id) {  // checking if user we want to follow is not current user
    try {
      const user = await User.findById(req.params.id); //find that user by id
      const currentUser = await User.findById(req.body.userId); //finding our current user
      if (!user.followers.includes(req.body.userId)) { //checking if that user's followers array contain that currentuser or not
        await user.updateOne({ $push: { followers: req.body.userId } }); //update followers array of that user
        await currentUser.updateOne({ $push: { followings: req.params.id } });
        res.status(200).json("user has been followed");
      } else {
        res.status(403).json("you allready follow this user");
      }
    } catch (err) {
      res.status(500).json(err);
    }
  } else {
    res.status(403).json("you cant follow yourself");
  }
});

//unfollow a user

router.put("/:id/unfollow", async (req, res) => { // all are same thing as our follow user but instead of push method we are using pull method
    if (req.body.userId !== req.params.id) {
      try {
        const user = await User.findById(req.params.id);
        const currentUser = await User.findById(req.body.userId);
        if (user.followers.includes(req.body.userId)) {
          await user.updateOne({ $pull: { followers: req.body.userId } });
          await currentUser.updateOne({ $pull: { followings: req.params.id } });
          res.status(200).json("user has been unfollowed");
        } else {
          res.status(403).json("you dont follow this user");
        }
      } catch (err) {
        res.status(500).json(err);
      }
    } else {
      res.status(403).json("you cant unfollow yourself");
    }
  });

